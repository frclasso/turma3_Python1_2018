"""Valores booleanos: Verdadeiro - Falso
                       True - False
                       1 - 0"""

# operadores
x = 5
y = 8

print('x == y: ', x == y) # retorna True se ambos as variaveis possuirem o mesmo valor
print('x != y: ', x != y) # diferente
print('x >= y: ', x >= y) # maior ou igual
print('x <= y: ', x <= y) # menor ou igual
print('x > y: ', x > y) # maior que
print('x < y: ', x < y) # menor que